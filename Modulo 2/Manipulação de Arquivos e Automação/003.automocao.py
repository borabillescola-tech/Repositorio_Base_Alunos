# Simule o movimento do mouse e um click

# Inciamos com a importação das bibliotecas
import pyautogui

# Move o mouse para a posição (100,200)
pyautogui.moveTo(100,200, duration=1)

# Realiza um click com o botão esquerdo do mouse
pyautogui.click() # Função que executa o click de um botão do mouse