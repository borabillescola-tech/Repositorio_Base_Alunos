# Dada a lista de nomes = ["Ana", "Carlos", "Maria"], use .insert()
# Para adicionar o nome "João" na segunda posição (índice 1).
# Mostre a lista no final

nomes = ["Ana", "Carlos", "Maria"] # lista inicial

nomes.insert(1, "João") # inserimos o nome "João" na segunda posição (índice 1)
print(nomes) # mostramos a lista com o nome adicionado