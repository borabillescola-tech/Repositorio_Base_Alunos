# clear()
# Dada a lista dados = [10, 20, 30, 40], use .clear() para apagar todos os
# elementos. Mostre o resultado final.

dados = [10, 20, 30, 40] # lista inicial
print(dados) # mostramos a lista inicial

dados.clear() # apagamos todos os elementos da lista
print(dados) # mostramos a lista após a remoção