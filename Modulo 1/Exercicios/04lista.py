# remove()
# Dada a lista cores = ["vermelho", "azul", "verde", "azul"], use
# .remove() para retirar apenas a primeira ocorrência de "azul". Mostre a lista
# antes e depois.

cores = ["vermelho", "azul", "verde", "azul"] # lista inicial
print(cores) # mostramos a lista inicial

cores.remove("azul") # removemos a primeira ocorrência de "azul"
print(cores) # mostramos a lista após a remoção