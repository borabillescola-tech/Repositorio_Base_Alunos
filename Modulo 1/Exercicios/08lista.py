# count()
# Dada a lista notas = [7, 8, 7, 10, 7, 6], use .count() para descobrir
# quantas vezes a nota 7 aparece. Mostre o resultado.

notas = [7, 8, 7, 10, 7, 6] # lista inicial
print(notas) # mostramos a lista inicial

quantidade = notas.count(7) # contamos quantas vezes 7 aparece
print("A nota 7 aparece", quantidade, "vezes.") # mostramos o resultado