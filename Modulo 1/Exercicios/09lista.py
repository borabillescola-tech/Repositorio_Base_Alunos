# sort() e reverse()
# Dada a lista numeros = [5, 2, 9, 1, 7], faça:
# ○ Use .sort() para ordenar em ordem crescente.
# ○ Depois use .reverse() para inverter a ordem.
# Mostre a lista após cada operação.

numeros = [5, 2, 9, 1, 7] # lista inicial
print("Lista inicial:", numeros)

numeros.sort() # ordenamos a lista
print("Após sort():", numeros)

numeros.reverse() # invertendo a lista
print("Após reverse():", numeros)