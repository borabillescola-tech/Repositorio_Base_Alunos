# index()
# Dada a lista letras = ["a", "b", "c", "d", "b"], use .index() para
# encontrar a posição da primeira ocorrência da letra "b". Mostre o índice retornado.

letras = ["a", "b", "c", "d", "b"] # lista inicial
print(letras) # mostramos a lista inicial

indice = letras.index("b") # encontramos o índice da primeira ocorrência de "b"
print("Índice da primeira ocorrência de 'b':", indice) # mostramos o índice encontrado