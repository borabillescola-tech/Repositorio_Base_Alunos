# pop()
# Dada a lista paises = ["Brasil", "Argentina", "Chile", "Peru"], use
# .pop() para remover o último elemento da lista e mostre qual foi removido e como
# ficou a lista.

paises = ["Brasil", "Argentina", "Chile", "Peru"] # lista inicial
print(paises) # mostramos a lista inicial

removido = paises.pop() # removemos o último elemento
print("Removido:", removido) # mostramos o elemento removido
print(paises) # mostramos a lista após a remoção