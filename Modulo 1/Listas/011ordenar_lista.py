# Como ordenar uma lista?

# .sort() Ordena os itens da lista (a lista original é modificada).
# lista = [3, 1, 2]; lista.sort()

lista_desordenada = [3, 1, 2]
print(lista_desordenada)

lista_desordenada.sort()
print(lista_desordenada)

letras = ['b', 'c', 'a']
letras.sort() # Ordem alfabética
print(letras)

